package com.example.superfit.models;

public class DiasModel {
    public int Id_dia ;
    public String Clave_dia ;
    public String Dia ;

    public int getId_dia() {
        return Id_dia;
    }

    public void setId_dia(int id_dia) {
        Id_dia = id_dia;
    }

    public String getClave_dia() {
        return Clave_dia;
    }

    public void setClave_dia(String clave_dia) {
        Clave_dia = clave_dia;
    }

    public String getDia() {
        return Dia;
    }

    public void setDia(String dia) {
        Dia = dia;
    }
}
