package com.example.superfit.models;

public class MesesModel {
    public int Id_mes ;
    public String Clave_mes ;
    public String Mes ;

    public int getId_mes() {
        return Id_mes;
    }

    public void setId_mes(int id_mes) {
        Id_mes = id_mes;
    }

    public String getClave_mes() {
        return Clave_mes;
    }

    public void setClave_mes(String clave_mes) {
        Clave_mes = clave_mes;
    }

    public String getMes() {
        return Mes;
    }

    public void setMes(String mes) {
        Mes = mes;
    }
}
